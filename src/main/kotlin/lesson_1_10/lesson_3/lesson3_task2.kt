fun main() {
    val firstName = "Татьяна"
    var secondName = "Андреева"
    val surname = "Сергеевнa"

    var years = 20

    println("$secondName $firstName $surname, $years лет")

    secondName = "Сидорова"
    years = 22

    println("$secondName $firstName $surname, $years года")
}